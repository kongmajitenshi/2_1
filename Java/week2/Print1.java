package week2;

// Print1의 P가 대문자로 작성되어야 한다.
/* 
 * 파일명과 클래스명은 
 * 반드시 동일해야한다.
 */
public class Print1 {
	// main 메소드는 반드시 있어야 한다.
	// JVM이 실행할 때 제일 먼저 찾아서 실행시킨다.
	// 자바 프로그램을 실행하는 진입점이다.
	
	// main 입력 후 ctrl + spacebar => 자동완성
	public static void main(String[] args) {
		System.out.println("Hello, Java!");
		// sysout 입력 후 ctrl + spacebar => 자동완성
		System.out.println("Hello, World!");
		// ctrl + F11 => 실행
	}
}
